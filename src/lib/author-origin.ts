function normalizeToken(t: string): string {
  return t.toLowerCase().replace(/[’'ʼ.,;:()]/g, '').trim();
}

/** True iff the token is a real name part, not an initial like "В.М." or "К." */
function isFullToken(t: string): boolean {
  const stripped = t.replace(/[.,;:()]/g, '');
  return stripped.length > 2;
}

/** True if ANY non-initial token in the author's name ends in a Slavic-surname
 *  suffix. We scan every token (not just last) because the catalog has both
 *  "First Last" and "Last First" ordering, plus "Surname Initials" rows. */
function hasSlavicSurname(name: string): boolean {
  for (const tok of name.trim().split(/[\s,]+/)) {
    if (!isFullToken(tok)) continue;
    const low = tok.toLowerCase().replace(/[’'ʼ]/g, "'");
    if (SLAVIC_SURNAME_SUFFIXES.some((suf) => low.endsWith(suf))) return true;
  }
  return false;
}

function hasSlavicFirstName(name: string): boolean {
  for (const t of name.trim().split(/[\s,]+/)) {
    const n = normalizeToken(t);
    if (n && SLAVIC_FIRST_NAMES.has(n)) return true;
  }
  return false;
}

export type AuthorOrigin = 'uk' | 'foreign';

/** Classify an author as Ukrainian/Slavic or foreign. Priority:
 *  1. Explicit foreign list → foreign (takes precedence over heuristics,
 *     so cases like "Эдгар По" don't get mis-flagged by first-name checks).
 *  2. Explicit UA list → uk
 *  3. Any book in literature-ukr → uk
 *  4. Any book in literature-foreign → foreign
 *  5. Slavic first name OR surname ending → uk (Ukrainian + Russian — per
 *     operator directive these belong with "наші", not with foreign/Western).
 *  6. Default → foreign. */
export function classifyAuthorOrigin(author: AuthorSummary): AuthorOrigin {
  if (FOREIGN_AUTHOR_NAMES.has(author.name)) return 'foreign';
  if (UKRAINIAN_AUTHOR_NAMES.has(author.name)) return 'uk';
  for (const b of author.books) {
    if (b.category === 'literature-ukr') return 'uk';
  }
  for (const b of author.books) {
    if (b.category === 'literature-foreign') return 'foreign';
  }
  if (hasSlavicFirstName(author.name) || hasSlavicSurname(author.name)) return 'uk';
  return 'foreign';
}
