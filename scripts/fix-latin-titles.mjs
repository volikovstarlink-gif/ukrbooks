// ─── apply ──────────────────────────────────────────────────────────
const apply = process.argv.includes('--apply');
const index = JSON.parse(readFileSync(INDEX_PATH, 'utf8'));
const bySlug = new Map(index.books.map((b) => [b.slug, b]));

let fullFixes = 0;
let titleOnlyFixes = 0;
let missing = [];

function writeBookFile(slug, patch) {
  const path = join(BOOKS_DIR, slug + '.json');
  if (!existsSync(path)) return false;
  const b = JSON.parse(readFileSync(path, 'utf8'));
  Object.assign(b, patch);
  if (apply) writeFileSync(path, JSON.stringify(b, null, 2) + '\n', 'utf8');
  return true;
}

for (const [slug, patch] of Object.entries(TIER_A)) {
  const indexEntry = bySlug.get(slug);
  if (!indexEntry) { missing.push(slug); continue; }
  Object.assign(indexEntry, patch);
  writeBookFile(slug, patch);
  fullFixes++;
}

for (const [slug, patch] of Object.entries(TIER_B)) {
  const indexEntry = bySlug.get(slug);
  if (!indexEntry) { missing.push(slug); continue; }
  Object.assign(indexEntry, patch);
  writeBookFile(slug, patch);
  titleOnlyFixes++;
}

console.log(`TIER A (full metadata): ${fullFixes}`);
console.log(`TIER B (title only): ${titleOnlyFixes}`);
if (missing.length) console.log(`MISSING slugs: ${missing.join(', ')}`);

if (apply) {
  writeFileSync(INDEX_PATH, JSON.stringify(index, null, 2) + '\n', 'utf8');
  console.log(`\nwrote ${INDEX_PATH}`);
} else {
  console.log('\n(dry-run — pass --apply to write changes)');
}
